# Kernel PCA
  #feature extraction for non-linear data problems 
  #we map data to a higher dimension using kernel trick, then extract new features 
  # if we apply a logistic regression model (which is a linear classifier) to this dataset and visualize the results, 
  #we will see that the decision boundary is straight (linear) but cannot separate the data well unless it is curved
  #i.e. we will need to use Kernel PCA to map data to a dimension where it is linearly separable 
  #note kernel PCA is also unsupervised. in KPCA we use kernel trick, then PCA


# Importing the dataset
dataset = read.csv('Social_Network_Ads.csv')
dataset = dataset[3:5]

# Splitting the dataset into the Training set and Test set
# install.packages('caTools')
library(caTools)
set.seed(123)
split = sample.split(dataset$Purchased, SplitRatio = 0.75)
training_set = subset(dataset, split == TRUE)
test_set = subset(dataset, split == FALSE)

# Feature Scaling
training_set[-3] = scale(training_set[-3])
test_set[-3] = scale(test_set[-3])


# Applying Kernel PCA
install.packages('kernlab')
library(kernlab)
kpca = kpca(~., training_set[-3], kernel = 'rbfdot', features = 2)
  #we chose 2 features, so that it will return 2 independent variables (so we can use the visualization code below)

training_set_pca = as.data.frame(predict(kpca, training_set))
  #only 2 columns in training_set_pca, 1 for each principal component/new extracted features/new ind var's
  #but we need the dep variable in training set for the next code sections

training_set_pca$Purchased = training_set$Purchased
  #we'll append the dp var from the original training_set into training_set_pca
  #this made a new column called Purchased in training_set_pca

test_set_pca = as.data.frame(predict(kpca, test_set))
test_set_pca$Purchased = test_set$Purchased
  #now we have our principal components and dep. var in the test set too


# Fitting Logistic Regression to the Training set
classifier = glm(formula = Purchased ~ .,
                 family = binomial,
                 data = training_set_pca)

# Predicting the Test set results
prob_pred = predict(classifier, type = 'response', newdata = test_set_pca[-3])
y_pred = ifelse(prob_pred > 0.5, 1, 0)

# Making the Confusion Matrix
cm = table(test_set_pca[, 3], y_pred > 0.5) 
  #we see mostly correct answers (~87% accuracy)


# Visualising the Training set results
library(ElemStatLearn)
set = training_set_pca
X1 = seq(min(set[, 1]) - 1, max(set[, 1]) + 1, by = 0.01)
X2 = seq(min(set[, 2]) - 1, max(set[, 2]) + 1, by = 0.01)
grid_set = expand.grid(X1, X2)
colnames(grid_set) = c('V1', 'V2')
prob_set = predict(classifier, type = 'response', newdata = grid_set)
y_grid = ifelse(prob_set > 0.5, 1, 0)
plot(set[, -3],
     main = 'Logistic Regression (Training set)',
     xlab = 'Age', ylab = 'Estimated Salary',
     xlim = range(X1), ylim = range(X2))
contour(X1, X2, matrix(as.numeric(y_grid), length(X1), length(X2)), add = TRUE)
points(grid_set, pch = '.', col = ifelse(y_grid == 1, 'dodgerblue', 'salmon'))
points(set, pch = 21, bg = ifelse(set[, 3] == 1, 'dodgerblue3', 'salmon3'))
  #we see nicer separation than we did with only linear regression


# Visualising the Test set results
library(ElemStatLearn)
set = test_set_pca
X1 = seq(min(set[, 1]) - 1, max(set[, 1]) + 1, by = 0.01)
X2 = seq(min(set[, 2]) - 1, max(set[, 2]) + 1, by = 0.01)
